/**
 * Verilen tarihe en yakın geçmişteki fiyatı statik veriden bulur.
 * (Hafta sonları veya tatil günleri için geriye doğru arama yapar)
 */
export const findPriceFromStaticData = (date: Date): number | null => {
  const data = historicalGoldPrices;
  let attemptDate = new Date(date.getTime());

  // En fazla 30 gün geriye doğru arayalım (ay ortası verilerini yakalamak için)
  for (let i = 0; i < 30; i++) {
    // YYYY-MM-DD formatına çevir
    const dateStr = attemptDate.toISOString().split('T')[0];
    
    // Veriyi [kaynak: 1] dosyasından kontrol et
    if (data[dateStr]) {
      return data[dateStr];
    }
    
    // Bir gün geriye git
    attemptDate.setDate(attemptDate.getDate() - 1);
  }

  // 30 gün geride veri bulunamazsa null dön
  return null;
};

/**
 * Statik verideki [kaynak: 1] en son (en güncel) fiyatı bulur.
 */
export const getLatestStaticPrice = (): number => {
  const dates = Object.keys(historicalGoldPrices);
  const latestDate = dates[dates.length - 1];
  return historicalGoldPrices[latestDate] || 5620.00;
};